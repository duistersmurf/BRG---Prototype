package model;

public class Table 
{
	private int ID;
	private String name;
	
	public Table(int _ID, String _name)
	{
		ID = _ID;
		setName(_name);
	}
	
	private void setName(String _name) 
	{
		name = _name;
	}
	
	public String getName()
	{
		return name;
	}
	
	public int getID()
	{
		return ID;
	}
}